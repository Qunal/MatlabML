% If row start and end points are not specified, define defaults
if nargin <= 3
    startRow = 2;
    endRow = 8400;
end

%% Import the data, extracting spreadsheet dates in MATLAB serial date number format (datenum)
[~, ~, raw1, dateNums1] = xlsread(workbookFile, sheetName, sprintf('A%d:A%d',startRow(1),endRow(1)),'' , @convertSpreadsheetDates);
[~, ~, raw2, dateNums2] = xlsread(workbookFile, sheetName, sprintf('P%d:P%d',startRow(1),endRow(1)),'' , @convertSpreadsheetDates);
