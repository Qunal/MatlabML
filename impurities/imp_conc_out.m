raw = [raw1,raw2,raw3];
dateNums = [dateNums1,dateNums2,dateNums3];
for block=2:length(startRow)
    [~, ~, tmpRawBlock1, tmpDateNumBlock1] = xlsread(workbookFile, sheetName, sprintf('A%d:A%d',startRow(block),endRow(block)),'' , @convertSpreadsheetDates);
    [~, ~, tmpRawBlock2, tmpDateNumBlock2] = xlsread(workbookFile, sheetName, sprintf('P%d:P%d',startRow(block),endRow(block)),'' , @convertSpreadsheetDates);
    [~, ~, tmpRawBlock3, tmpDateNumBlock3] = xlsread(workbookFile, sheetName, sprintf('V%d:V%d',startRow(block),endRow(block)),'' , @convertSpreadsheetDates);
    raw = [raw;tmpRawBlock1,tmpRawBlock2,tmpRawBlock3]; %#ok<AGROW>
    dateNums = [dateNums;tmpDateNumBlock1,tmpDateNumBlock2,tmpDateNumBlock3]; %#ok<AGROW>
end
raw(cellfun(@(x) ~isempty(x) && isnumeric(x) && isnan(x),raw)) = {''};

